    //Load vendors into the table
    private void loadCustomers() {
        DefaultTableModel model = (DefaultTableModel) vendorTable.getModel();
        
        for (Vendor vendor : vendors) {
            Object[] rowData = { vendor.getName(), vendor.getPhoneNumber(), vendor.getEmail()
                    , vendor.getPassword(), vendor.getDescription(), 
                    vendor.getOperationHours(), vendor.getOperationDays()};
            model.addRow(rowData);
        }
    }
    
